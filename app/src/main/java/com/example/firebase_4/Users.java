package com.example.firebase_4;

// Used to ADD User

public class Users {

    String name,date;

    public Users() {
    }

    public Users(String name, String date) {
        this.name = name;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
