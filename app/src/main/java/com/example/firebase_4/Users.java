package com.example.firebase_4;

// Used to ADD User


public class Users {

    String name,age;

    public Users() {
    }

    public Users(String name, String age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
